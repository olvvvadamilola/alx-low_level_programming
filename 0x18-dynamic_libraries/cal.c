#include <stdio.h>
/**
 * add - Function
 * @a: input
 * @b: input
 * Return: a func b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Function
 * @a: input
 * @b: input
 * Return: a func b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - Function
 * @a: input
 * @b: input
 * Return: a func b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Function
 * @a: input
 * @b: input
 * Return: a func b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function
 * @a: input
 * @b: input
 * Return: modulus
 */
int mod(int a, int b)
{
	return (a % b);
}
